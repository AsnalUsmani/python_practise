# List of colors
colors = ["Red", "Blue", "Green", "Yellow", "Orange", "Violet", "Indigo"]

# Sort in ascending order
colors_ascending = sorted(colors)  # Default sorting is ascending
print("Colors in Ascending Order:", colors_ascending)

# Sort in descending order
colors_descending = sorted(colors, reverse=True)  # Reverse for descending
print("Colors in Descending Order:", colors_descending)


